public class GhBlock {

private Actor actor;
private String type;
private Repo repo;

    public Actor getActor() {
        return actor;
    }



    public void setActor(Actor actor) {
        this.actor = actor;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Repo getRepo() {
        return repo;
    }

    public void setRepo(Repo repo) {
        this.repo = repo;
    }
}
